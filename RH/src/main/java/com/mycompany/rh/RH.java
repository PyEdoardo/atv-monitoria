    private double calcGerente(int id){
        Gerente gerente = new Gerente();
        double folha = 0;
        for (Funcionario func : listaFunc){
            if (func.getId() == id){
                if (func instanceof Gerente){
                    gerente = (Gerente) func;
                }
            }
        }
        
        for (Funcionario func : listaFunc){
            if (func instanceof Desenvolvedor){
                if (((Desenvolvedor) func).getIdChefe() == gerente.getId()){
                    folha += ((Desenvolvedor) func).calcularBonus();
                }
            }
        }
        
        return folha;
    }
   
    private Funcionario acharFunc(int id){
        for (Funcionario func : listaFunc){
            if (func.getId() == id){
                return func;
            }
        }
        return null;
    }
   
    private Funcionario acharFunc(String nome){
        for (Funcionario func : listaFunc){
            if (func.getNome().equalsIgnoreCase(nome)){
                return func;
            }
        }
        return null;
    }

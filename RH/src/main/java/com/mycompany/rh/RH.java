    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (btn_nome.isSelected()){
            var nomeProcura = JOptionPane.showInputDialog("Digite o nome do funcionário");
            
            var func = acharFunc(nomeProcura);
            
            if (func != null){
               JOptionPane.showMessageDialog(null, func.toString());
            }
            else {
                 JOptionPane.showMessageDialog(null, "Funcionário não encontrado!");
            }
        }
        
        else if (btn_ID.isSelected()){
            var idProcura = Integer.parseInt(JOptionPane.showInputDialog("Digite o ID: "));
            
            var func = acharFunc(idProcura);
            
            if (func != null){
                JOptionPane.showMessageDialog(null, func.toString());
            }
            else {
                JOptionPane.showMessageDialog(null, "Funcionário não encontrado!");
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Selecione um dos métodos de procura!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (radio_bonus.isSelected()){
            JOptionPane.showMessageDialog(null, apenasBonus());
        }
        else if (radio_bruto.isSelected()){
            JOptionPane.showMessageDialog(null, salarioBruto());
        }
        else if (radio_total.isSelected()){
            JOptionPane.showMessageDialog(null, salarioBruto() + apenasBonus());
        }
        else {
            JOptionPane.showMessageDialog(null, "Selecione algum botão!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


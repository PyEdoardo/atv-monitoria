    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        var tituloProc = JOptionPane.showInputDialog("Digite o título da obra: ");
        Obra obra = procurarObra(tituloProc);
        int index = procurarIndex(obra);
        if (obra != null){
            var novoTitulo = JOptionPane.showInputDialog("Digite o novo nome da obra: ");
            var novoAutor = JOptionPane.showInputDialog("Digite o novo autor: ");
            var novoData = Integer.parseInt(JOptionPane.showInputDialog("Digite a nova data: "));
            obra.setAutor(novoAutor);
            obra.setTitulo(novoTitulo);
            obra.setDataPub(novoData);
            listaObras.set(index, obra);
        }
        else {
            JOptionPane.showMessageDialog(null, "Obra inválida!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


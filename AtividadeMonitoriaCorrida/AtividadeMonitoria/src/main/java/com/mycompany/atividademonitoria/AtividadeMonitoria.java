    private void adicionarNoCodigo(){
        listaObras.add(new Artigo(1, "CEULP/ULBRA", "Uso de IA nas Escolas", "Edoardo", 2026));
        listaObras.add(new Livro(80, "Aventura", "The Witcher: Torre da Andorinha", "Andrew Sapkowsky", 1990));
        listaObras.add(new Revista(72, "Mensal", "Criação de Jogos", "Igor", 2026));
    }

    
    private Obra procurarObra(String titulo){
        for (var obra : listaObras){
            if (obra.getTitulo().equalsIgnoreCase(titulo)){
                return obra;
            }
        }
        JOptionPane.showMessageDialog(null, "Obra não encontrada!");
        return null;
    }
   
    private int procurarIndex(Obra obraI){
        var num = 0;
        for (var obra : listaObras){
            if (obra.getTitulo().equalsIgnoreCase(obraI.getTitulo())){
                return num;
            }
            num++;
        }
        return num;
    }
    

    private void btn_mostrar_acervoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrar_acervoActionPerformed
        var retorno = "";
        for (var obra : listaObras){
            if (obra instanceof Artigo){
                retorno = retorno + "\nTipo: Artigo \n" + obra.exibirResumo();
            }
            else if (obra instanceof Livro){
                retorno = retorno + "\nTipo: Livro \n" + obra.exibirResumo();
            }
            else {
                try {
                    retorno = retorno + "\nTipo: Revista\n" + obra.exibirResumo();
                } catch (Exception e){
                    e.printStackTrace();
                    System.err.println("Obra é Nula: " + e.getMessage());
                }
            }            
        }
        JOptionPane.showMessageDialog(null, retorno);
    }//GEN-LAST:event_btn_mostrar_acervoActionPerformed

